package com.example.app;

public class Calculator {

    public int add(int a, int b) {

        int total = a + b;
        System.out.println("Total a+b = " + total);
        return total;
    }

    public int subtract(int a, int b) {

        int total = a - b;
        System.out.println("Total a-b = " + total);
        return total;
    }


}
