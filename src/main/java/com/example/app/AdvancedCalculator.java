package com.example.app;

public class AdvancedCalculator {
    public int multiply(int a, int b) {

        int total = a * b;
        System.out.println("Total a*b = " + total);
        return total;
    }

    public int divide(int a, int b) {

        int total = a / b;
        System.out.println("Total a/b = " + total);
        return total;
    }
}
